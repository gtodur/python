import json

car = {"company": "Suzuki", "model": "Celerio"}
print(json.dumps(car, indent=2))      # dumps() converts python dictionary to json string

numbers_present = {1: True, 2: True, 3: False}
print(json.dumps(numbers_present))

print('=====================================================================')
#########################################################################################
############################## PYTHON DATA TO JSON FILE #################################
#########################################################################################
person_data = {
    "name": "Joe Smith",
    "age": 25,
    "address": {
        "building_street": "#12, James Corner",
        "line2": "Brandon Street",
        "city" : "Rochdale",
        "postcode": "L10AB"
    },
    "cars": [
        "Honda", "BMW", "Mercedes"
    ]
}

with open("person_data.json", mode='w', encoding="utf-8") as f:
    json.dump(person_data, f, indent=2, sort_keys=True)

with open("person_data.json", 'r') as f:
    print(f.read())

print('=====================================================================')
####################################################################################
############################## JSON TO PYTHON DATA #################################
